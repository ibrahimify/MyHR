def set_language(lang_code: str):
    """Call this on login when user picks a language."""
    global _current_language
    if lang_code in TRANSLATIONS:
        _current_language = lang_code


def get_language() -> str:
    return _current_language


def t(key: str, **kwargs) -> str:
    """
    Translate a key to the current language.
    Usage: t("save")  →  "Save" / "Mentés" / "حفظ"
    With variables: t("salary_increment_prompt", count=5)
    Falls back to English if key missing in current language.
    """
    lang = TRANSLATIONS.get(_current_language, TRANSLATIONS["en"])
    text = lang.get(key) or TRANSLATIONS["en"].get(key, key)
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, IndexError):
            return text
    return text


def is_rtl() -> bool:
    """Returns True if current language is right-to-left (Arabic)."""
    return _current_language == "ar"